/**
 * Enrich static video titles/descriptions by fetching oEmbed metadata.
 * This is a lightweight, key-free YouTube endpoint — safe to call server-side.
 * Falls back gracefully if the request fails.
 */
export async function enrichVideosWithOEmbed(
  videos: Video[]
): Promise<Video[]> {
  const results = await Promise.allSettled(
    videos.map(async (video) => {
      try {
        const url = `https://www.youtube.com/oembed?url=https://www.youtube.com/watch?v=${video.youtubeVideoId}&format=json`;
        const res = await fetch(url, { next: { revalidate: 86400 } });
        if (!res.ok) return video;
        const data = await res.json();
        return {
          ...video,
          title: data.title || video.title,
          channelName: data.author_name || video.channelName,
          thumbnailUrl:
            `https://i.ytimg.com/vi/${video.youtubeVideoId}/hqdefault.jpg`,
        };
      } catch {
        return video;
      }
    })
  );

  return results.map((r, i) =>
    r.status === "fulfilled" ? r.value : videos[i]
  );
}
